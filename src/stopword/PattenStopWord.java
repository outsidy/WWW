	/**
	 * Pattern 기반의 불용어 추출<br/>
	 */
	public static boolean isStopWord(final String Term) {
		// 0. 비었을 때
		if (Term == null || Term.length() == 0) {
			reason = "\t 0. 비었을 때";
			return true;
		}

		// 1. 45자보다 길때
		if (Term.length() >= 45) {
			reason = "\t 1. 45자보다 길때";
			return true;
		}

		// 2. 영문 불용어
		if (ENGSTOP_SET.contains(Term)) {
			reason = "\t 2. 영문 불용어";
			return true;
		}

		// 3. 한글자 단어
		if (Term.length() == 1 || Term.matches("^[ㄱ-ㅎㅏ-ㅣ가-힣]$")) {
			reason = "\t 3. 한글자 단어";
			return true;
		}

		Pattern ptn = Pattern.compile("-?\\d+([.,~]\\d+)?", Pattern.MULTILINE);

		Matcher matcher = ptn.matcher(Term);

		// 4. 숫자로만 구성된 단어 (숫자 . - )
		if (matcher.find()) {
			reason = "\t 4. 숫자로만 구성된 단어 (숫자 . - )";
			return true;
		}

		ptn = Pattern.compile(".*[^ㄱ-ㅎㅏ-ㅣ가-힣|a-z|A-Z]$", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 5. 글자가 아닌 기호로 끝난 단어
		if (matcher.find()) {
			reason = "\t 5. 글자가 아닌 기호로 끝난 단어";
			return true;
		}

		ptn = Pattern.compile("^[^ㄱ-ㅎㅏ-ㅣ가-힣|a-z|A-Z|0-9]+$", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 6. 기호로만 구성된 단어
		if (matcher.find()) {
			reason = "\t 5. 글자가 아닌 기호로 끝난 단어";
			return true;
		}

		ptn = Pattern.compile("-?\\d+(\\.\\d+)?\\D+", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 7. 숫자 + 의존명사
		if (matcher.find()) {
			reason = "\t 7. 숫자 + 의존명사";
			return true;
		}

		ptn = Pattern.compile("[\\D]_.*[\\D]", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// // 8. 한글자_여러글자
		// if (matcher.find()) {
		// return "\t 8. 한글자_여러글자";
		// }

		ptn = Pattern.compile("\\d+[\\D]_.*[\\D]]", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 9. 숫자+글자_글자
		if (matcher.find()) {
			reason = "\t 9. 숫자+글자_글자";
			return true;
		}

		ptn = Pattern.compile("-?\\d+([.,~]?\\d+)[_\\-=]\\w+", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 10. 숫자+기호+글자
		if (matcher.find()) {
			reason = "\t 10. 숫자+기호+글자";
			return true;
		}

		ptn = Pattern.compile("-?\\d+([.,~]?\\d+)\\W+", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 11. 3~4주
		if (matcher.find()) {
			reason = "\t 11. 3~4주";
			return true;
		}

		ptn = Pattern.compile("^[ㄱ-ㅎㅏ-ㅣ가-힣]+[^ㄱ-ㅎㅏ-ㅣ가-힣 _]+[ㄱ-ㅎㅏ-ㅣ가-힣]+$", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 12. 한글 시작과 끝 사이에 한글을 제외한 글자가 들어간 경우
		if (matcher.find()) {
			reason = "\t 12. 한글 시작과 끝 사이에 한글을 제외한 글자가 들어간 경우";
			return true;
		}

		ptn = Pattern.compile("^[^a-zA-Zㄱ-ㅎㅏ-ㅣ가-힣]+", Pattern.MULTILINE);
		matcher = ptn.matcher(Term);

		// 13. 영한으로 시작되지 않는 경우
		if (matcher.find()) {
			reason = "\t 13. 영한으로 시작되지 않는 경우";
			return true;
		}

		// 14. stopcontain : 일치하면
		for (int i = 0; i < stopContain.length; i++) {
			ptn = Pattern.compile(stopContain[i], Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			// 14. stopcontain : 일치하면
			if (matcher.find()) {
				reason = "\t 14. stopcontain : 일치하면(" + stopContain[i] + ")";
				return true;
			}
		}

		// 15. stoppattern : 패턴이 일치하면
		for (int i = 0; i < stopPattern.length; i++) {
			ptn = Pattern.compile(stopPattern[i], Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			// 15. stoppattern : 패턴이 일치하면
			if (matcher.find()) {
				reason = "\t 15. stoppattern : 패턴이 일치하면(" + stopPattern[i] + ")";
				return true;
			}
		}

		// 16. stoppostfix : 끝나는 패턴
		for (String s : stopPostfix) {
			ptn = Pattern.compile(s + "$", Pattern.MULTILINE);
			matcher = ptn.matcher(Term);
			if (matcher.find()) {
				reason = "\t 16. stoppostfix : 끝나는 패턴(" + s + ")";
				return true;
			}
		}

		// 17. stopsegment : 중간 포함단어
		for (int i = 0; i < stopSegment.length; i++) {
			ptn = Pattern.compile("^" + stopSegment[i] + "$", Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			if (matcher.find()) {
				reason = "\t 17. stopsegment : 중간 포함단어 ^XXX$(" + stopSegment[i] + ")";
				return true;
			}

			ptn = Pattern.compile("^" + stopSegment[i] + "_", Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			if (matcher.find()) {
				reason = "\t 17. stopsegment : 중간 포함단어 ^XXX_(" + stopSegment[i] + ")";
				return true;
			}

			ptn = Pattern.compile("_" + stopSegment[i] + "$", Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			if (matcher.find()) {
				reason = "\t 17. stopsegment : 중간 포함단어 _XXX$(" + stopSegment[i] + ")";
				return true;
			}

			ptn = Pattern.compile("_" + stopSegment[i] + "_", Pattern.MULTILINE);
			matcher = ptn.matcher(Term);

			if (matcher.find()) {
				reason = "\t 17. stopsegment : 중간 포함단어 _XXX_(" + stopSegment[i] + ")";
				return true;
			}
		}

		// 18. stopPrefix : 시작 패턴
		for (String s : stopPrefix) {
			ptn = Pattern.compile("^" + s, Pattern.MULTILINE);
			matcher = ptn.matcher(Term);
			if (matcher.find()) {
				reason = "\t 18. stopPrefix : 시작 패턴(" + s + ")";
				return true;
			}
		}
		return false;
	}

